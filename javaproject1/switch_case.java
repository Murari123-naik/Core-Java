package javaproject1;

public class switch_case {

	public static void main(String[] args) {
		
		int Age=25;
		
		switch (Age) {
		
		case 10:
		System.out.println("Age is less than 25");
		break;
		
		case 20:
			System.out.println("Age is less than 25");
			break;
			
		case 25:
			System.out.println("Age is 25");
			
		
		}
		
		

	}

}
